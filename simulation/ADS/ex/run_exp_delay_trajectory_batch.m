%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 批量运行三组实测滤波器时延轨迹提取
% 用途：
% 1. 依次运行三份 lowpassfilter_filter*.csv 数据
% 2. 自动导出每份数据对应的对比图、混合图和调试 CSV
% 3. 在终端打印简要汇总，便于横向比较
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
clc; clear; close all;

script_dir = fileparts(mfilename('fullpath'));

%% 1. 批处理配置
data_file_list = { ...
    'lowpassfilter_filter.csv', ...
    'lowpassfilter_filter_1.csv', ...
    'lowpassfilter_filter_2.csv' ...
    };

case_title_list = { ...
    '原始数据', ...
    '实测数据 1', ...
    '实测数据 2' ...
    };

out_suffix_list = { ...
    '_base', ...
    '_1', ...
    '_2' ...
    };

batch_summaries = cell(numel(data_file_list), 1);

%% 2. 逐份运行
for i_case = 1:numel(data_file_list)
    fprintf('\n============================================================\n');
    fprintf('【批处理】%d/%d : %s\n', i_case, numel(data_file_list), case_title_list{i_case});
    fprintf('  数据文件: %s\n', data_file_list{i_case});
    fprintf('============================================================\n');

    data_file = data_file_list{i_case};
    case_title = case_title_list{i_case};
    out_suffix = out_suffix_list{i_case};

    run(fullfile(script_dir, 'extract_exp_delay_trajectory_adaptive.m'));
    batch_summaries{i_case} = result_summary;
end

%% 3. 汇总输出
fprintf('\n================ 批处理汇总 ================\n');
fprintf('  %-10s %-12s %-10s %-10s %-10s %-10s %-10s\n', ...
    '数据标签', 'base点数', 'adapt点数', 'merged点数', ...
    '左边缘', '右边缘', 'a_cal');

for i_case = 1:numel(batch_summaries)
    summary_i = batch_summaries{i_case};
    fprintf('  %-10s %-12d %-10d %-10d %-10d %-10d %-10.4f\n', ...
        summary_i.case_title, ...
        summary_i.base_points, ...
        summary_i.adapt_points, ...
        summary_i.hybrid_points, ...
        summary_i.left_edge_points, ...
        summary_i.right_edge_points, ...
        summary_i.base_a_cal);
end

fprintf('\n导出文件命名后缀对应关系：\n');
for i_case = 1:numel(out_suffix_list)
    fprintf('  %-10s -> %s\n', case_title_list{i_case}, out_suffix_list{i_case});
end
